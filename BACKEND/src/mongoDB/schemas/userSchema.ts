import { Schema, model } from "mongoose";
import IUser from "../interfaces/IUser";

const UserSchema = new Schema({
    email: {
        type: String,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        required: true
    }
});

export default model<IUser>('users', UserSchema);